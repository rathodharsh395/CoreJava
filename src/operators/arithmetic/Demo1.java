package operators.arithmetic;

public class Demo1 {
    public static void main(String[] args) {
      int number1 = 15;
      int number2 = 27;
      System.out.println(number1 + number2);//110
        System.out.println(number1 - number2);//90
        System.out.println(number1 * number2);//1000
        System.out.println(number1 / number2);//Quotient = 10
        System.out.println(number1 % number2);//Reminder or Modulo = 0

        //System.out.println(10/0);
       // System.out.println(10%0);
        System.out.println(10.0/0);//Infinity
        System.out.println(10.0%0);//NaN: Not a number


    }
}
